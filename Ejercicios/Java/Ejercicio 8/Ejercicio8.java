public class Ejercicio8 {
    public static void main (String[] args){
        int numero = 25;

        numero += 5;
        System.out.println("(numero += 5) = " + numero);

        numero -= 15;
        System.out.println("(numero -= 15) = " + numero);

        numero *= 2;
        System.out.println("(numero *= 2) = " + numero);

        numero /= 2;
        System.out.println("(numero /= 2) = " + numero);

        numero %= 3;
        System.out.println("(numero %= 3) = " + numero);
    }
}
